<template>
  <div class="tab-control">
    <div v-for="(item,index) in title" 
    class="tab-control-item"
     :class="{active:index===currentIndex}"
     @click="itemclick(index)">
      <span>{{ item }}</span>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    title: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data(){
      return{
          currentIndex:0
      }
  },
  methods:{
      itemclick(index){
       this.currentIndex=index,
      this.$emit('itemclick',index)
      }
  }
};
</script>
